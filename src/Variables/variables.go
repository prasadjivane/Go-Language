package main

import "fmt"

func main()  {

	var p=10  // variable declaration one or more
	fmt.Println(p)

	var a,b int=2,3  // Multiple variable declaration
	fmt.Println(a,b)

	var c=true      //variable Type Declaration
	fmt.Println(c)
	
	var d int       //variable declaration without initialization (zero) value
	fmt.Println(d)

	e:="Long"      // variable declaration with initialization
	fmt.Println(e)
}